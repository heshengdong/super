package zyl.model;

public class TuiHuoStock {
	private String guid;
	
	private String tuiHuoCode;
	
	private String stockId;
	
	private String providerName;
	
	private String stockPrice;
	
	private String tuihuoNumber;
	
	private String tuihuoAllPrice;
	
	private String date;
	
	private String yuanYin;
	
	private String username;
	
	
	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getGuid() {
		return guid;
	}

	public void setGuid(String guid) {
		this.guid = guid;
	}

	public String getStockId() {
		return stockId;
	}

	public void setStockId(String stockId) {
		this.stockId = stockId;
	}

	public String getProviderName() {
		return providerName;
	}

	public void setProviderName(String providerName) {
		this.providerName = providerName;
	}

	public String getStockPrice() {
		return stockPrice;
	}

	public void setStockPrice(String stockPrice) {
		this.stockPrice = stockPrice;
	}

	public String getTuihuoNumber() {
		return tuihuoNumber;
	}

	public void setTuihuoNumber(String tuihuoNumber) {
		this.tuihuoNumber = tuihuoNumber;
	}

	public String getTuihuoAllPrice() {
		return tuihuoAllPrice;
	}

	public void setTuihuoAllPrice(String tuihuoAllPrice) {
		this.tuihuoAllPrice = tuihuoAllPrice;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getYuanYin() {
		return yuanYin;
	}

	public void setYuanYin(String yuanYin) {
		this.yuanYin = yuanYin;
	}

	public String getTuiHuoCode() {
		return tuiHuoCode;
	}

	public void setTuiHuoCode(String tuiHuoCode) {
		this.tuiHuoCode = tuiHuoCode;
	}
}
